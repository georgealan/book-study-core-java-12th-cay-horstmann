package Data_Types;

/** STUDY NOTES
 * The integer parts are from numbers without fractional parts. Negative values are allowed, are 4 interger types
 * exitent, they are: int, short, long and byte.
 *
 */

public class IntegerTypes {
    public static void main(String[] args) {
        // Example variables with integer types
        byte peopleInHome; // 1 byte, Range is: -128 to 127
        short peopleInCity; // 2 bytes, Range is: -32,768 to 32,767
        int peopleInState; // 4 bytes, Range is: -2,147,483,648 to 2,147,483,647 (just over 2 billion)
        long peopleInCountry; // 8 bytes, Range is: -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807



    }
}
